package com.randoworks.coryatkeeper.ui.main;

public class FJRoundStats {
    private int fjWager;
    private Boolean correct;

    public FJRoundStats() {
        this.correct = null;
    }

    public int getFjWager() {
        return fjWager;
    }

    public void setFjWager(int fjWager) {
        this.fjWager = fjWager;
    }

    public Boolean getCorrect() {
        return correct;
    }

    public void setCorrect(Boolean correct) {
        this.correct = correct;
    }
}
